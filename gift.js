// 선물 추천 상태
let giftOccasion = '';

// 선물 추천 시작
function startGiftRecommendation() {
    $("#gift-person").text(personLabel);
    $("#gift-mbti").text(mbti);

    $(".phrase-selector").hide();
    $(".gift-occasion-selector").show();
    window.scrollTo(0, 0);
}

// 상황 선택
function selectGiftOccasion(occasion) {
    giftOccasion = occasion;
    showGiftResult();
}

// 선물 추천 결과 표시
function showGiftResult() {
    const data = giftData[giftOccasion][mbti];

    $("#gift-occasion-title").text(giftOccasion);
    $("#gift-person-label").text(personLabel);
    $("#gift-mbti-label").text(mbti);

    // 추천 선물
    const recommendedHTML = data.recommended.map(item => `<li>${item}</li>`).join('');
    $("#gift-recommended-list").html(recommendedHTML);

    // 피해야 할 선물
    const avoidHTML = data.avoid.map(item => `<li>${item}</li>`).join('');
    $("#gift-avoid-list").html(avoidHTML);

    $(".gift-occasion-selector").hide();
    $(".gift-result").show();
    window.scrollTo(0, 0);
}

// 선물 추천에서 돌아가기
function backToPhrasesFromGift() {
    $(".gift-result").hide();
    $(".phrase-selector").show();
    window.scrollTo(0, 0);
}

// 선물 추천에서 처음으로
function restartFromGift() {
    $(".gift-result").hide();
    $(".gift-occasion-selector").hide();
    $(".start").show();
    window.scrollTo(0, 0);
}
